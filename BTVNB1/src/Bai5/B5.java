package Bai5;

import java.util.Scanner;

public class B5 {
	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
		System.out.print("Nhập số nguyên n: ");
		int n = in.nextInt();
		System.out.print("Nhập số nguyên m: ");
		int m = in.nextInt();
		System.out.println(n + " + " + m + " = " + (m + n));
	}
}
